제 6 조 (서비스내용변경 통지 등)

1. 회사가 서비스 내용을 변경하거나 종료하는 경우 회사는 회원에게 개별적으로 전자적 형태(전자우편, SMS 등) 등의 방법을 통해 서비스 내용의 변경 사항 또는 종료를 통지할 수 있습니다.

2. 제1항의 경우 불특정 다수인을 상대로 통지를 함에 있어서는 홈페이지 등 기타 회사의 공지사항을 통하여 회원들에게 통지할 수 있습니다.

 

제 7 조 (서비스이용의 제한 및 중지)

1. 회사는 아래 각 호의 1에 해당하는 사유가 발생한 경우에는 회원의 서비스 이용을 제한하거나 중지시킬 수 있습니다.

    ① 회원이 회사 서비스의 운영을 고의 또는 중과실로 방해하는 경우
    ② 서비스용 설비 점검, 보수 또는 공사로 인하여 부득이한 경우
    ③ 전기통신사업법에 규정된 기간통신사업자가 전기통신 서비스를 중지했을 경우
    ④ 국가비상사태, 서비스 설비의 장애 또는 서비스 이용의 폭주 등으로 서비스 이용에 지장이 있는 때
    ⑤ 기타 중대한 사유로 인하여 회사가 서비스 제공을 지속하는 것이 부적당하다고 인정하는 경우

2. 회사는 전항의 규정에 의하여 서비스의 이용을 제한하거나 중지한 때에는 그 사유 및 제한기간 등을 회원에게 알려야 합니다.

 

제 8 조 (개인위치정보의 이용 또는 제공)

1. 회사는 개인위치정보를 이용하여 서비스를 제공하고자 하는 경우에는 미리 이용약관에 명시한 후 개인위치정보주체의 동의를 얻어야 합니다.

2. 회원 및 법정대리인의 권리와 그 행사방법은 제소 당시의 이용자의 주소에 의하며, 주소가 없는 경우에는 거소를 관할하는 지방법원의 전속관할로 합니다. 다만, 제소 당시 이용자의 주소 또는 거소가 분명하지 않거나 외국 거주자의 경우에는 민사소송법상의 관할법원에 제기합니다.

3. 회사는 제4조에 따른 서비스 제공 및 회원의 동네 미션 업무 수행 기록 분석, 통계작성 등의 목적으로 개인위치정보를 보유하며, 이를 위하여 개인위치정보를 회원 탈퇴시까지 보관합니다.

4. 회사는 위치정보의 보호 및 이용 등에 관한 법률 제16조 제2항에 근거하여 개인위치정보주체에 대한 위치정보 수집ㆍ이용ㆍ제공사실 확인자료를 위치정보시스템에 자동으로 기록하며, 해당자료는 [6개월] 간 보관합니다.

